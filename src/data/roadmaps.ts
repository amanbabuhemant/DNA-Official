// Helper function to get all roadmaps as a flat array
export const getAllRoadmaps = () => {
  return [
    ...roadmapData["role-based"],
    ...roadmapData["skill-based"], 
    ...roadmapData["best-practices"]
  ];
};

// Helper function to get roadmaps by category
export const getRoadmapsByCategory = (category: string) => {
  return roadmapData[category as keyof typeof roadmapData] || [];
};

// Helper function to get featured roadmaps
export const getFeaturedRoadmaps = () => {
  return getAllRoadmaps().filter(roadmap => (roadmap as any).featured);
};

// Helper function to search roadmaps
export const searchRoadmaps = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return getAllRoadmaps().filter(roadmap => 
    roadmap.title.toLowerCase().includes(lowercaseQuery) ||
    roadmap.description.toLowerCase().includes(lowercaseQuery) ||
    roadmap.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};
